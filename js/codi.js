document.getElementById("sliderVol").oninput = canviaVol;

function canviaVol() {
    //Assignem el valor de l'slader al volum dereproducció
    document.getElementById("musica").volume =
        document.getElementById("sliderVol").value;
};

//Quan carreguem la pàgina assignem a l'slider el valor que tingui el volum
document.getElementById("sliderVol").value =
    document.getElementById("musica").volume;






document.getElementById("sliderDur").max =
    document.getElementById("musica").duration;


//Fem que quan es toqui l'slider s'executi la funció canviaTemps
document.getElementById("sliderDur").oninput = canviaTemps;

function canviaTemps() {
    //Assignem el valor de l'slader al volum dereproducció
    document.getElementById("musica").currentTime =
        document.getElementById("sliderDur").value;
};

